import Foundation
import Vapor
import Fluent

class ProductCategorySchema {
    static let schema: String = "ProductCategories"
    
    static func createBuilder(database: Database) -> SchemaBuilder {
        database.schema(Self.schema)
            .id()
            .field("name", .string, .required)
            .unique(on: "name")
    }
}
